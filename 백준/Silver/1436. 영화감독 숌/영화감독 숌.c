int main(void) {
    char input[100];

    fgets(input, sizeof(input), stdin);

    int N = parseInt(input);

    int idx = 1;

    // 현재 몇 번째 종말 숫자인지
    int currEndNumbers = 0;
    // 바로 최근의 종말 숫자는 무엇인지
    int currEndNumber = 0;

    while(N != currEndNumbers) {
        if(isEndNum(idx) == 1) {
            currEndNumbers++;
            currEndNumber = idx;
        }

        idx++;
    }

    char* result = (char*)intToStr(currEndNumber);

    fputs(result, stdout); fputc('\n', stdout);

    free(result);

    return 0;
}

_Bool isEndNum(int number) {
    char* strNum = intToStr(number);
    char* strNumPtr = (char*)strNum;

    _Bool isEndNumber = 0;

    int currSixLen = 0;
    char preCh = ' ';

    while(*strNumPtr) {
        char ch = *strNumPtr++;

        if(ch == '6') {
            currSixLen = (preCh == '6') ? currSixLen + 1 : 1;
        } else {
            currSixLen = 0;
        }

        preCh = ch;

        if(currSixLen == 3) {
            isEndNumber = 1;
            break;
        }
    }

    free(strNum);

    return isEndNumber;
}

char* intToStr(int target) {
    int sign = 1;
    int len = 0;

    if(target < 0) {
        sign = -1;
        target *= -1;
    }

    int temp = target;
    do {
        temp /= 10;
        len++;
    } while(temp != 0);

    int size = sign == 1 ? len : len + 1;
    int endIdx = size - 1;
    int startIdx = sign == 1 ? 0 : 1;

    char* result = (char*)malloc(sizeof(char) * (size + 1));
    *(result + size) = 0;

    int idx = endIdx;
    while(idx >= startIdx) {
        *(result + idx--) = (target % 10) + '0';
        target /= 10;
    }
    if(sign == -1) {
        *result = '-';
    }

    return result;
}
int parseInt(const char* str) {
    int sign = 1;
    int result = 0;

    char* strPtr = (char*)str;
    if(*strPtr == '-') {
        sign = -1;
        strPtr++;
    }

    while(isBlank(*strPtr) != 1) {
        result *= 10;
        result += *strPtr++ - '0';
    }

    return result * sign;
}
_Bool isBlank(char ch) {
    if(ch == 32 || ch == 0) {
        return 1;
    } else if(ch >= 9 && ch <= 13) {
        return 1;
    } else {
        return 0;
    }
}
