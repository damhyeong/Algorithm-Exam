#include<stdio.h>

extern void* malloc(size_t byte);
extern void* calloc(size_t size, size_t byte);
extern void free(void* memory);

char* intToStr(int target);
int parseInt(const char* str);
_Bool isBlank(char ch);

int main (void) {
    char input[100];

    int size = 30;
    int* students = (int*)calloc(size + 1, sizeof(int));
    int* stdPtr = (int*)students;

    while(fgets(input, sizeof(input), stdin) != NULL) {
        int currNum = parseInt(input);

        *(students + currNum - 1) = 1;
    }

    int idx = 0;
    while(idx++ < size) {
        _Bool isSumbit = *stdPtr++;

        if(isSumbit == 0) {
            char* tempResult = intToStr(idx);

            fputs(tempResult, stdout); fputc('\n', stdout);

            free(tempResult);
        }
    }

    free(students);

    return 0;
}

char* intToStr(int target) {
    int sign = 1;
    int len = 0;

    if(target < 0) {
        sign = -1;
        target *= sign;
    }

    int temp = target;
    do {
        temp /= 10;
        len++;
    } while (temp != 0);

    int size = sign == 1 ? len : len + 1;
    int endIdx = size - 1;
    int startIdx = sign == 1 ? 0 : 1;

    char* result = (char*)malloc(sizeof(char) * (size + 1));
    result[size] = '\0';

    int i = endIdx;
    while(i >= startIdx) {
        result[i--] = target % 10 + '0';
        target /= 10;
    }
    if(sign == -1) {
        *result = '-';
    }

    return result;
}
int parseInt(const char* str) {
    int sign = 1;
    int result = 0;

    char* strPtr = (char*)str;

    if(*strPtr == '-') {
        sign = -1;
        strPtr++;
    }
    while(isBlank(*strPtr) != 1) {
        result *= 10;
        result += *strPtr++ - '0';
    }

    return result * sign;
}
_Bool isBlank(char ch) {
    if(ch == 32 || ch == '\0') {
        return 1;
    } else if(ch >= 9 && ch <= 13) {
        return 1;
    } else {
        return 0;
    }
}
